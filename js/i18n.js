const zhMonthNumbers = {
  Jan: 1,
  Feb: 2,
  Mar: 3,
  Apr: 4,
  May: 5,
  Jun: 6,
  Jul: 7,
  Aug: 8,
  Sep: 9,
  Oct: 10,
  Nov: 11,
  Dec: 12
};


function translateTripText(value) {
  if (
    language !== "zh" ||
    typeof value !== "string" ||
    value === ""
  ) {
    return value;
  }

  if (zhTripText[value]) {
    return zhTripText[value];
  }

  const dateMatch =
    value.match(
      /^(\d{1,2}) ([A-Z][a-z]{2}) 2026$/
    );

  if (
    dateMatch &&
    zhMonthNumbers[dateMatch[2]]
  ) {
    return (
      `2026年${zhMonthNumbers[dateMatch[2]]}` +
      `月${Number(dateMatch[1])}日`
    );
  }

  const morningDateMatch =
    value.match(
      /^(\d{1,2}) ([A-Z][a-z]{2}) morning$/
    );

  if (
    morningDateMatch &&
    zhMonthNumbers[morningDateMatch[2]]
  ) {
    return (
      `${zhMonthNumbers[morningDateMatch[2]]}` +
      `月${Number(morningDateMatch[1])}日上午`
    );
  }

  const nextMorningMatch =
    value.match(
      /^(\d{1,2}:\d{2}) AM next morning$/
    );

  if (nextMorningMatch) {
    return `隔天上午${nextMorningMatch[1]}`;
  }

  const onDateMatch =
    value.match(
      /^(\d{1,2}:\d{2}) AM on (\d{1,2}) ([A-Z][a-z]{2})$/
    );

  if (
    onDateMatch &&
    zhMonthNumbers[onDateMatch[3]]
  ) {
    return (
      `${zhMonthNumbers[onDateMatch[3]]}` +
      `月${Number(onDateMatch[2])}` +
      `日上午${onDateMatch[1]}`
    );
  }

  const timeMatch =
    value.match(
      /^(\d{1,2}:\d{2}) (AM|PM)$/
    );

  if (timeMatch) {
    return (
      `${timeMatch[2] === "AM" ? "上午" : "下午"}` +
      `${timeMatch[1]}`
    );
  }

  const hourMinuteMatch =
    value.match(
      /^(\d+) hr (\d+) mins$/
    );

  if (hourMinuteMatch) {
    return (
      `${hourMinuteMatch[1]}小时` +
      `${hourMinuteMatch[2]}分钟`
    );
  }

  const hourRangeMatch =
    value.match(
      /^(\d+(?:–\d+(?:\.\d+)?)?) hrs?\+?$/
    );

  if (hourRangeMatch) {
    return (
      `${hourRangeMatch[1]}小时` +
      `${value.endsWith("+") ? "以上" : ""}`
    );
  }

  const decimalHoursMatch =
    value.match(
      /^(\d+(?:\.\d+)?) hrs$/
    );

  if (decimalHoursMatch) {
    return `${decimalHoursMatch[1]}小时`;
  }

  const minutesMatch =
    value.match(
      /^(\d+) mins$/
    );

  if (minutesMatch) {
    return `${minutesMatch[1]}分钟`;
  }

  const distanceMatch =
    value.match(
      /^(\d+(?:\.\d+)?) km$/
    );

  if (distanceMatch) {
    return `${distanceMatch[1]}公里`;
  }

  return value;
}


function getLocalizedTripDay(day) {
  if (!day || language !== "zh") {
    return day;
  }

  return {
    ...day,

    originalCity: day.city,

    displayDate:
      translateTripText(day.displayDate),

    weekday:
      translateTripText(day.weekday),

    city:
      translateTripText(day.city),

    title:
      translateTripText(day.title),

    summary:
      translateTripText(day.summary),

    accommodation:
      day.accommodation
        ? {
            ...day.accommodation,

            name:
              translateTripText(
                day.accommodation.name
              ),

            room:
              translateTripText(
                day.accommodation.room
              ),

            checkIn:
              translateTripText(
                day.accommodation.checkIn
              ),

            checkOut:
              translateTripText(
                day.accommodation.checkOut
              )
          }
        : null,

    drive:
      day.drive
        ? {
            ...day.drive,

            from:
              translateTripText(
                day.drive.from
              ),

            to:
              translateTripText(
                day.drive.to
              ),

            duration:
              translateTripText(
                day.drive.duration
              )
          }
        : null,

    timeline:
      (day.timeline || []).map(item => ({
        ...item,

        time:
          translateTripText(item.time),

        duration:
          translateTripText(item.duration),

        title:
          translateTripText(item.title),

        desc:
          translateTripText(item.desc)
      })),

    places:
      (day.places || []).map(place => ({
        ...place,

        originalName:
          place.name,

        originalCity:
          place.city || day.city,

        name:
          translateTripText(place.name),

        city:
          translateTripText(
            place.city || day.city
          )
      })),

    food:
      (day.food || []).map(item => ({
        ...item,

        name:
          translateTripText(item.name),

        note:
          translateTripText(item.note)
      })),

    reminders:
      (day.reminders || [])
        .map(translateTripText),

    notes:
      (day.notes || [])
        .map(translateTripText)
  };
}


